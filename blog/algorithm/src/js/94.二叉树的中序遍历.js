/**
 * @param {TreeNode} root
 * @return {number[]}
 */
 function inorderTraversal(root) {
    const stack = []; //存储节点的栈
    const res = []; //存储中序的数组
  
    //迭代
    while (root || stack.length) {
      if (root) {
        //不断找极左点，将中间点压入栈中
        stack.push(root);
        root = root.left;
      } else {
        root = stack.pop();
        res.push(root.val);
        root = root.right;
      }
    }
  
    return res;
  }
